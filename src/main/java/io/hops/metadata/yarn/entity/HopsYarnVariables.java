package io.hops.metadata.yarn.entity;

public class HopsYarnVariables {

    private int id;
    private int value;

    public HopsYarnVariables(int id, int value) {
        this.id = id;
        this.value = value;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getValue() {
        return value;
    }

    public void setValue(int value) {
        this.value = value;
    }

}
